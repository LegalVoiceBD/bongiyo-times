export default function LocationFilter({ layout = 'horizontal' }: { layout?: 'horizontal' | 'vertical' }) {
  const router = useRouter();
  const [division, setDivision] = useState('');
  const [district, setDistrict] = useState('');
  const [upazila, setUpazila] = useState('');

  const divisions = Object.keys(bdLocations);
  const districts = division ? Object.keys(bdLocations[division]) : [];
  const upazilas = district ? bdLocations[division][district] : [];

  const handleSearch = (e: React.FormEvent) => {
    e.preventDefault();
    const query = upazila || district || division;
    if (!query) return;
    router.push(`/?category=বাংলাদেশ&q=${encodeURIComponent(query)}`);
  };

  const selectClass = "w-full border border-[#cbd5e1] p-2.5 text-[15px] font-medium rounded-sm focus:outline-none focus:border-[#104f96] bg-white cursor-pointer appearance-none bg-[url('data:image/svg+xml;charset=US-ASCII,%3Csvg%20width%3D%2220%22%20height%3D%2220%22%20xmlns%3D%22http%3A%2F%2Fwww.w3.org%2F2000%2Fsvg%22%3E%3Cpath%20d%3D%22M7%2010l5%205%205-5%22%20fill%3D%22none%22%20stroke%3D%22%23666%22%20stroke-width%3D%221.5%22%20stroke-linecap%3D%22round%22%20stroke-linejoin%3D%22round%22%2F%3E%3C%2Fsvg%3E')] bg-no-repeat bg-[position:right_10px_center]";

  return (
    <form onSubmit={handleSearch} className={`flex ${layout === 'horizontal' ? 'flex-col md:flex-row' : 'flex-col'} gap-3 w-full`}>
      <select 
        value={division} 
        onChange={(e) => { setDivision(e.target.value); setDistrict(''); setUpazila(''); }} 
        className={selectClass}
      >
        <option value="">বিভাগ</option>
        {divisions.map(div => <option key={div} value={div}>{div} বিভাগ</option>)}
      </select>

      <select 
        value={district} 
        onChange={(e) => { setDistrict(e.target.value); setUpazila(''); }} 
        className={selectClass} 
        disabled={!division}
      >
        <option value="">জেলা</option>
        {districts.map(dist => <option key={dist} value={dist}>{dist}</option>)}
      </select>

      <select 
        value={upazila} 
        onChange={(e) => setUpazila(e.target.value)} 
        className={selectClass} 
        disabled={!district}
      >
        <option value="">উপজেলা</option>
        {upazilas.map(upa => <option key={upa} value={upa}>{upa}</option>)}
      </select>

      <button 
        type="submit" 
        disabled={!division} 
        className={`bg-[#104f96] text-white font-bold py-2.5 px-6 rounded-sm transition flex items-center justify-center gap-2 shrink-0 ${!division ? 'opacity-50 cursor-not-allowed' : 'hover:bg-[#0c3e78]'}`}
      >
        <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2.5" strokeLinecap="round" strokeLinejoin="round"><circle cx="11" cy="11" r="8"></circle><line x1="21" y1="21" x2="16.65" y2="16.65"></line></svg> 
        খুঁজুন
      </button>
    </form>
  );
}
